numero = int(input("Digite um número: "))
if numero % 2 != 0:
    print("%s é impar" %(numero))
else:
    print("%s não é impar" %(numero))
if numero % 3 == 0:
    print("%s é múltiplo de 3" %(numero))
else:
    print("%s não é múltiplo de 3" %(numero))
if 102 % numero == 0:
    print("%s é divisor de 102" %(numero))
else:
    print("%s não é divisor de 102" %(numero))
    
    
   
